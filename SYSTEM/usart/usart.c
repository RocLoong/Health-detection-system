void Usart_SendByte( USART_TypeDef * pUSARTx, uint8_t ch)
{
	
	printf("[debug] %s  %d:\r\n", __FILE__, __LINE__);
	/* 等待发送数据寄存器为空 */
	printf("[debug] char is %x %s  %d:\r\n",ch, __FILE__, __LINE__);
	/* 发送一个字节数据到USART */
	USART_SendData(pUSARTx,ch);
	printf("[debug] %s  %d:\r\n", __FILE__, __LINE__);
	while (USART_GetFlagStatus(pUSARTx, USART_FLAG_TXE) == RESET);	

	printf("[debug] %s  %d:\r\n", __FILE__, __LINE__);


}

void USART2_IRQHandler()
{
	u8 Res;
	static u8 flag;
	if(USART_GetITStatus(USART2, USART_IT_RXNE) != RESET)
	{
		USART_ClearITPendingBit(USART2,USART_IT_RXNE);
		Res =USART_ReceiveData(USART2);						//(USART1->DR);	//读取接收到的数据
		if (Res==(0xff)) 									//当数据包的首字节为0xff,则继续接收,否则丢弃
		{
			flag = 1;
		}
		if(flag)
		{
			USART_HRV_RX_BUF[USART_HRV_RX_STA]=Res;					//每次进中断在这里赋值给数组
			USART_HRV_RX_STA++;																		
		}
		if(USART_HRV_RX_STA>(USART_HRV_LEN-1))					//达到76字节时,回到数组第1位
		{
			USART_HRV_RX_STA=0;
			flag= 0;
			senor_Flag=1;
		}
		   		 
     } 
}
